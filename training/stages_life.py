age = 3
if age < 2:
    print("You are a baby!")
if age >= 2 and age < 4:
    print("You are a toddler!")
if age >= 4 and age < 13:
    print("You are a kid!")
if age >= 13 and age < 20:
    print("You are a teenager!")
if age >= 20 and age < 65:
    print("You are an adult!")
if age >= 65:
    print('You are an elder!')